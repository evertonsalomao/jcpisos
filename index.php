                    </div>
                   <h5 class="mb-1 ">Preparação do Solo:</h5>
                    <div class="instalacao-text text-left ">
                        <p>Essa é a etapa mais crítica. Primeiro, fazemos a escavação do solo na área que será pavimentada. A profundidade da escavação depende do tipo de tráfego que o piso vai suportar (por exemplo, um estacionamento de carros precisa de uma base mais profunda do que um caminho para pedestres). Depois da escavação, o solo é nivelado e compactado para garantir que a superfície esteja firme e homogênea, sem buracos ou ondulações.</p>
                       
                    </div>
                </div>

                <div class="instalacao-item text-center">
                    <div class="instalacao-img position-relative">
                        <img class="img-fluid mx-auto " src="img/instalacao/2.png">
                        <div class="numero-circulo">02</div>
                    </div>
                   <h5 class="mb-1 ">Implantação do subleito</h5>
                    <div class="instalacao-text text-left ">
                        <p>Sobre o solo compactado, aplicamos uma camada de bica corrida (BGS) ou rachão sendo nivelado com caimento para ralos e grelhas em seguida será compactada para dar a máxima estabilidade ao conjunto. Essa camada serve como uma base de reforço.</p>
                       
                    </div>
                </div>
                <div class="instalacao-item text-center">
                    <div class="instalacao-img position-relative">
                       <img class="img-fluid mx-auto " src="img/instalacao/3.png">
                        <div class="numero-circulo">03</div>
                    </div>
                   <h5 class="mb-1 ">Instalação das Guias ou Meio-Fio de contenção</h5>
                    <div class="instalacao-text text-left ">
                        <p>Antes de colocar o piso, instalamos a guia (meio-fio) no perímetro da área. Ela é fundamental para conter as peças do piso e evitar que elas se movam com o tempo.</p>
                       
                    </div>
                </div>

                <div class="instalacao-item text-center">
                    <div class="instalacao-img position-relative">
                        <img class="img-fluid mx-auto " src="img/instalacao/4.png">
                        <div class="numero-circulo">04</div>
                    </div>
                   <h5 class="mb-1 ">Nivelamento da área com uma Camada de Pó de Pedra ou Areia</h5>
                    <div class="instalacao-text text-left ">
                        <p>Espalhamos uma camada em torno de 4 cm de pó de pedra ou areia sendo nivelada com réguas. Essa camada é a responsável por acomodar cada peça de piso intertravado de forma perfeita, corrigindo pequenas imperfeições e garantindo um assentamento uniforme.</p>
                       
                    </div>
                </div>

                <div class="instalacao-item text-center">
                    <div class="instalacao-img position-relative">
                       <img class="img-fluid mx-auto " src="img/instalacao/5.png">
                       <div class="numero-circulo">05</div> 
                    </div>
                   <h5 class="mb-1 ">Assentamento do Piso Intertravado</h5>
                    <div class="instalacao-text text-left ">
                        <p>Os pisos são assentados manualmente seguindo o padrão escolhido pelo cliente (como a espinha de peixe ou a amarração).</p>
                       
                    </div>
                </div>

                <div class="instalacao-item text-center">
                    <div class="instalacao-img position-relative">
                        <img class="img-fluid mx-auto " src="img/instalacao/6.png">
                        <div class="numero-circulo">06</div>
                    </div>
                   <h5 class="mb-1 ">Arremates e cortes</h5>
                    <div class="instalacao-text text-left ">
                        <p>Para acabamento final e profissional ao projeto os recortes e arremates são feitos com uma serra de disco diamantado. Ela é fundamental para garantir que o piso se encaixe perfeitamente em torno de guias, bueiros, ralos e outros obstáculos.</p>
                       
                    </div>
                </div>

                 <div class="instalacao-item text-center">
                    <div class="instalacao-img position-relative">
                        <img class="img-fluid mx-auto" src="img/instalacao/7.png" alt="guia padrão prefeituras">
                       <div class="numero-circulo">07</div>
                    </div>
                    <h5 class="mb-1 ">Rejuntamento com Pó de Pedra ou Areia</h5>
                     <div class="instalacao-text text-left ">
                        <p>Depois da instalação e arremates do piso intertravado, espalhamos uma camada de pó de pedra ou areia sobre toda a superfície. Usando vassoura  para varrer o pó de pedra ou areia e direcioná-la para dentro das juntas, garantindo que cada fresta entre as peças seja totalmente preenchida. O Rejunte é o que realmente "trava" as peças e garante a estabilidade e a durabilidade do pavimento.</p>
                       
                    </div>
                </div>

                <div class="instalacao-item text-center">
                    <div class="instalacao-img position-relative">
                        <img class="img-fluid mx-auto " src="img/instalacao/8.png" alt="guia padrão prefeituras">
                       <div class="numero-circulo">08</div>
                    </div>
                    <h5 class="mb-1 ">Compactação e limpeza final</h5>
                     <div class="instalacao-text text-left ">
                        <p>Para a compactação utilizamos uma placa vibratória que irá vibrar a superfície dos blocos. A vibração faz com que o pó de pedra ou areia de rejunte desça e se acomode preenchendo completamente as juntas, nivelando os pisos e travando as peças umas nas outras e para a finalização será dado mais uma mão de rejunte e limpeza da obra.</p>
                       
                    </div>
                </div>

                
